def get_ip_list(url, headers):
    req = requests.get(url=url, headers=header)
    soup = BeautifulSoup(req.text, 'lxml')
    ips = soup.find_all('tr')
    ip_address_pool = list()
    for i in range(1, len(ips)):
        content = ips[i]
        string = content.text
        info_list = string.split('\n')
        if (info_list[3] != '80') and (info_list[8] == 'HTTP'):
            ip = info_list[2]+':'+info_list[3]
            ip_address_pool.append(ip)
    if len(ip_address_pool):
        return ip_address_pool
    else:
        print('无法获取ip代理')
        return None
